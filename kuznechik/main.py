# -------------------------------
# 2. Вектор для линейного преобразования (L-функции)
# -------------------------------
# Определён в ГОСТ, используется при расчёте R-преобразования
linear_vector = bytes([
    0x94,0x20,0x85,0x10,0xC2,0xC0,0x01,0xFB,
    0x01,0xC0,0xC2,0x10,0x85,0x20,0x94,0x01
])

# -------------------------------
# 3. Основные операции
# -------------------------------

def X(a: bytearray, b: bytearray) -> bytearray:
    """Побитовое XOR двух 16-байтных блоков"""
    return bytearray(x ^ y for x, y in zip(a, b))

def S(state: bytearray) -> None:
    """S-преобразование: замена каждого байта по таблице Pi"""
    for i in range(KUZNECHIK_BLOCK_SIZE):
        state[i] = Pi[state[i]]

def S_reverse(state: bytearray) -> None:
    """Обратное S-преобразование (для дешифровки)"""
    for i in range(KUZNECHIK_BLOCK_SIZE):
        state[i] = Pi_reverse[state[i]]

def GF_mult(a: int, b: int) -> int:
    """
    Умножение в поле Галуа GF(2^8) по неприводимому многочлену
    x^8 + x^7 + x^6 + x + 1 (0xC3)
    Используется в R-преобразовании.
    """
    c = 0
    for _ in range(8):
        if b & 1:
            c ^= a
        high_bit = a & 0x80
        a = (a << 1) & 0xFF
        if high_bit:
            a ^= 0xC3
        b >>= 1
    return c

def R(state: bytearray) -> None:
    """
    R-преобразование: сдвиг байтов вправо с "смешиванием".
    Умножает каждый байт на свой коэффициент и суммирует по модулю 2.
    """
    acc = state[15]
    for i in range(14, -1, -1):
        state[i + 1] = state[i]
        acc ^= GF_mult(state[i], linear_vector[i])
    state[0] = acc

def R_reverse(state: bytearray) -> None:
    """Обратное R-преобразование (для дешифровки)"""
    acc = state[0]
    for i in range(0, 15):
        state[i] = state[i + 1]
        acc ^= GF_mult(state[i], linear_vector[i])
    state[15] = acc

def L(state: bytearray) -> None:
    """L-преобразование = 16 последовательных R-преобразований"""
    for _ in range(KUZNECHIK_BLOCK_SIZE):
        R(state)

def L_reverse(state: bytearray) -> None:
    """Обратное L-преобразование"""
    for _ in range(KUZNECHIK_BLOCK_SIZE):
        R_reverse(state)

# -------------------------------
# 4. Генерация итерационных ключей (раундовых)
# -------------------------------
def gen_round_keys(key: bytes) -> List[bytearray]:
    """
    Разбивает 256-битный ключ на два 128-битных и
    вычисляет 10 итерационных ключей.
    Используется тот же принцип, что и в алгоритме ГОСТ.
    """
    if len(key) != 32:
        raise ValueError("Ключ должен быть длиной 32 байта (256 бит).")

    # Генерация 32 вспомогательных констант C_i
    cs = [bytearray(KUZNECHIK_BLOCK_SIZE) for _ in range(32)]
    for i in range(32):
        cs[i][15] = (i + 1) & 0xFF  # последний байт = номер итерации
        L(cs[i])  # применяем L для "распределения" бита по всему вектору

    # Разделяем ключ на две половины (k1, k2)
    ks0 = bytearray(key[:16])
    ks1 = bytearray(key[16:32])

    round_keys = [bytearray(16) for _ in range(10)]
    round_keys[0] = ks0[:]
    round_keys[1] = ks1[:]

    # Генерация остальных ключей
    for i in range(1, 33):
        new_key = X(ks0, cs[i - 1])  # XOR с константой
        S(new_key)
        L(new_key)
        new_key = X(new_key, ks1)
        ks1 = ks0
        ks0 = new_key
        # Каждые 8 итераций добавляем новые ключи
        if i % 8 == 0:
            idx = i >> 2  # i/4
            round_keys[idx]     = ks0[:]
            round_keys[idx + 1] = ks1[:]
    return round_keys

# -------------------------------
# 5. Шифрование и дешифровка одного блока
# -------------------------------
def kuznechik_encrypt(round_keys: List[bytearray], data_block: bytes) -> bytearray:
    """Шифрование одного 128-битного блока"""
    if len(data_block) != 16:
        raise ValueError("Блок должен быть 16 байт.")
    p = bytearray(data_block)
    for i in range(10):
        p = X(p, round_keys[i])  # добавляем раундовый ключ
        if i < 9:                # последние 9 раундов: S+L
            S(p)
            L(p)
    return p

def kuznechik_decrypt(round_keys: List[bytearray], data_block: bytes) -> bytearray:
    """Расшифровка одного 128-битного блока"""
    if len(data_block) != 16:
        raise ValueError("Блок должен быть 16 байт.")
    p = bytearray(data_block)
    p = X(p, round_keys[9])
    for i in range(8, -1, -1):
        L_reverse(p)
        S_reverse(p)
        p = X(p, round_keys[i])
    return p

# -------------------------------
# 6. Служебные функции
# -------------------------------
def print_chunk(ch: bytes) -> None:
    """Вывод блока в шестнадцатеричном виде"""
    print(" ".join(f"0x{b:02X}" for b in ch))

# -------------------------------
# 7. Тестовый пример
# -------------------------------

def encrypt_text(text: str, key: bytes) -> bytes:

    text_bytes = text.encode('utf-8')
    round_keys = gen_round_keys(key)

    padding_len = KUZNECHIK_BLOCK_SIZE - (len(text_bytes) % KUZNECHIK_BLOCK_SIZE)
    if padding_len == 0:
        padding_len = KUZNECHIK_BLOCK_SIZE
    
    padded_data = text_bytes + bytes([padding_len] * padding_len)
    
    # шифрование блоков
    encrypted_blocks = []
    for i in range(0, len(padded_data), KUZNECHIK_BLOCK_SIZE):
        block = padded_data[i:i + KUZNECHIK_BLOCK_SIZE]
        encrypted_block = kuznechik_encrypt(round_keys, block)
        encrypted_blocks.append(encrypted_block)
    
    return b''.join(encrypted_blocks)

def decrypt_text(encrypted_data: bytes, key: bytes) -> str:
    round_keys = gen_round_keys(key)
    
    decrypted_blocks = []
    for i in range(0, len(encrypted_data), KUZNECHIK_BLOCK_SIZE):
        block = encrypted_data[i:i + KUZNECHIK_BLOCK_SIZE]
        decrypted_block = kuznechik_decrypt(round_keys, block)
        decrypted_blocks.append(decrypted_block)
    
    decrypted_data = b''.join(decrypted_blocks)
    padding_len = decrypted_data[-1]
    decrypted_data = decrypted_data[:-padding_len]
    
    return decrypted_data.decode('utf-8')

